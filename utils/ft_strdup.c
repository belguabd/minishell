#include "../minishell.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	int		i;
	char	*string;

	len = ft_strlen(s1);
	string = (char *)malloc((len + 1) * sizeof(char));
	if (!string)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		string[i] = s1[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
